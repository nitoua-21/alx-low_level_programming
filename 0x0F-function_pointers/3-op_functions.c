/**
 * op_add - computes the sum of a and b
 * @a: a
 * @b: b
 *
 * Return: sum of a and b.
 *
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - computes the difference of a and b.
 * @a: a
 * @b: b
 *
 * Return: the difference of a and b.
 *
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - computes the product of a and b.
 * @a: a
 * @b: b
 *
 * Return: product of a and b.
 *
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - computes the division of a by b.
 * @a: a
 * @b: b
 *
 * Return:  the result of the division of a by b.
 *
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - computes the remainder of the division of a by b.
 * @a: a
 * @b: b
 *
 * Return: remainder of the division of a by b.
 *
 */
int op_mod(int a, int b)
{
	return (a % b);
}
